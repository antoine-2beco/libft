#include "libft.h"

size_t	ft_arraylen(char **s)
{
	size_t	i;

	if (!s)
		return (0);
	i = 0;
	while (s[i])
	{
		if (i >= SIZE_T_MAX)
			return (0);
		i++;
	}
	return (i);
}
